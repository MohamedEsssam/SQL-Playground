/**
  ROUND/TRUNCATE
  second argument for
  procedure for rounding
 */
SELECT ROUND(5.7342423, 2);
SELECT ROUND(5.2);
SELECT TRUNCATE(5.7342423, 2);

/**
  CEILING will return the
  smallest int that greater than this number
 */
SELECT CEILING(5.2);

/**
  CEILING will return the
  smallest int that less than this number
 */
SELECT FLOOR(5.2);

/**
  ABS return absolute value
 */
SELECT ABS(-2);

